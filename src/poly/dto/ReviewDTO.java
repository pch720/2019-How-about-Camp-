package poly.dto;

public class ReviewDTO {

	private String reviewSeq;
	private String userSeq;
	private String reviewContent;
	private String reviewWriter;
	private String reviewWritedate;
	private String reviewUpdate;
	private String reviewUpnum;
	private String reviewSp;
	private String campName;
	public String getReviewSp() {
		return reviewSp;
	}
	public void setReviewSp(String reviewSp) {
		this.reviewSp = reviewSp;
	}
	public String getCampName() {
		return campName;
	}
	public void setCampName(String campName) {
		this.campName = campName;
	}
	public String getReviewSeq() {
		return reviewSeq;
	}
	public void setReviewSeq(String reviewSeq) {
		this.reviewSeq = reviewSeq;
	}
	public String getUserSeq() {
		return userSeq;
	}
	public void setUserSeq(String userSeq) {
		this.userSeq = userSeq;
	}
	public String getReviewContent() {
		return reviewContent;
	}
	public void setReviewContent(String reviewContent) {
		this.reviewContent = reviewContent;
	}
	public String getReviewWriter() {
		return reviewWriter;
	}
	public void setReviewWriter(String reviewWriter) {
		this.reviewWriter = reviewWriter;
	}
	public String getReviewWritedate() {
		return reviewWritedate;
	}
	public void setReviewWritedate(String reviewWritedate) {
		this.reviewWritedate = reviewWritedate;
	}
	public String getReviewUpdate() {
		return reviewUpdate;
	}
	public void setReviewUpdate(String reviewUpdate) {
		this.reviewUpdate = reviewUpdate;
	}
	public String getReviewUpnum() {
		return reviewUpnum;
	}
	public void setReviewUpnum(String reviewUpnum) {
		this.reviewUpnum = reviewUpnum;
	}

}
